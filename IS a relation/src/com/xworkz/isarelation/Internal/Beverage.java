package com.xworkz.isarelation.Internal;

public class Beverage {
    public Beverage() {
        System.out.println("Running no-arg constructor of Beverage");
    }

    public void prepare() {
        System.out.println("Preparing the beverage");
    }

    public void serve() {
        System.out.println("Serving the beverage");
    }

    public void addSugar() {
        System.out.println("Adding sugar to the beverage");
    }

    public void stir() {
        System.out.println("Stirring the beverage");
    }

    public void drink() {
        System.out.println("Drinking the beverage");
    }

}
