package com.xworkz.isarelation.Internal;

public class Element {
    public Element() {
        System.out.println("Running no-arg constructor of Element");
    }

    public void existInNature() {
        System.out.println("Element exists naturally or is synthesized");
    }

    public void combineWithOtherElements() {
        System.out.println("Elements can combine to form compounds");
    }

    public void hasAtomicStructure() {
        System.out.println("Each element has a unique atomic structure");
    }

    public void classifiedByType() {
        System.out.println("Elements are classified as metals, non-metals, or metalloids");
    }

    public void periodicTablePresence() {
        System.out.println("Elements are listed in the periodic table");
    }
}
