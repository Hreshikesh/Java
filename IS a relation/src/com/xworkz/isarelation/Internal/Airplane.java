package com.xworkz.isarelation.Internal;

public class Airplane{
    public Airplane() {
        System.out.println("Running no-arg constructor of Airplane");
    }

    public void takeOff() {
        System.out.println("Airplane is taking off");
    }

    public void fly() {
        System.out.println("Airplane is flying in the sky");
    }

    public void land() {
        System.out.println("Airplane is landing");
    }

    public void fuelCapacity() {
        System.out.println("Airplane has a large fuel capacity");
    }

    public void passengers() {
        System.out.println("Airplane carries passengers");
    }
}
