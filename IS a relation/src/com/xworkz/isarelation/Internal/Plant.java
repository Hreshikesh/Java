package com.xworkz.isarelation.Internal;

public class Plant {
    public Plant() {
        System.out.println("Running no-arg constructor of Plant");
    }

    public void grow() {
        System.out.println("The plant is growing");
    }

    public void absorbSunlight() {
        System.out.println("The plant is absorbing sunlight");
    }

    public void photosynthesize() {
        System.out.println("The plant is photosynthesizing");
    }

    public void reproduce() {
        System.out.println("The plant is reproducing");
    }

    public void consumeWater() {
        System.out.println("The plant is consuming water");
    }
}
