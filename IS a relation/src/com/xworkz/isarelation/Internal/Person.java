package com.xworkz.isarelation.Internal;

public class Person {
    public Person() {
        System.out.println("Running no-arg constructor of Person");
    }

    public void eat() {
        System.out.println("Person eats food");
    }

    public void sleep() {
        System.out.println("Person sleeps at night");
    }

    public void walk() {
        System.out.println("Person can walk");
    }

    public void talk() {
        System.out.println("Person can talk");
    }

    public void breathe() {
        System.out.println("Person breathes to live");
    }
}
