package com.xworkz.isarelation.Internal;

public class Display {
    public Display() {
        System.out.println("Running no-arg constructor of Display");
    }

    public void showImage() {
        System.out.println("Display shows images");
    }

    public void resolution() {
        System.out.println("Display has different resolutions");
    }

    public void refreshRate() {
        System.out.println("Display has a refresh rate");
    }

    public void brightness() {
        System.out.println("Display has adjustable brightness");
    }

    public void displayType() {
        System.out.println("Display types include LCD, LED, OLED, etc.");
    }
}
