public class ColdDrink {
    public ColdDrink() {
        System.out.println("Running no-arg constructor of ColdDrink");
    }

    public void drink() {
        System.out.println("ColdDrink is refreshing");
    }

    public void chill() {
        System.out.println("ColdDrink is best served chilled");
    }

    public void flavor() {
        System.out.println("ColdDrink comes in different flavors");
    }

    public void fizz() {
        System.out.println("ColdDrink has fizzy bubbles");
    }

    public void sugarContent() {
        System.out.println("ColdDrink may contain sugar or be sugar-free");
    }
}
