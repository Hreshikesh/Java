package com.xworkz.isarelation.Internal;

public class Box {
    public Box() {
        System.out.println("Running no-arg constructor of Box");
    }

    public void storeItems() {
        System.out.println("Box is used to store items");
    }

    public void open() {
        System.out.println("Box can be opened");
    }

    public void close() {
        System.out.println("Box can be closed");
    }

    public void material() {
        System.out.println("Box can be made of wood, plastic, or metal");
    }

    public void size() {
        System.out.println("Box comes in different sizes");
    }
}
