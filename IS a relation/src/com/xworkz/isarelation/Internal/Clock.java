package com.xworkz.isarelation.Internal;

public class Clock {
    public Clock() {
        System.out.println("Running no-arg constructor of Clock");
    }

    public void showTime() {
        System.out.println("Clock displays the time");
    }

    public void setAlarm() {
        System.out.println("Clock allows setting an alarm");
    }

    public void tick() {
        System.out.println("Clock makes ticking sound");
    }

    public void displayFormat() {
        System.out.println("Clock supports 12-hour and 24-hour formats");
    }

    public void powerSource() {
        System.out.println("Clock can be powered by battery or electricity");
    }
}
