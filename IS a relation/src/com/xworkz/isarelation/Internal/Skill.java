package com.xworkz.isarelation.Internal;

public class Skill {
    public Skill() {
        System.out.println("Running no-arg constructor of Skill");
    }

    public void learn() {
        System.out.println("Skill can be learned through practice");
    }

    public void improve() {
        System.out.println("Skill improves with experience and training");
    }

    public void apply() {
        System.out.println("Skill can be applied in real-world situations");
    }

    public void requiresEffort() {
        System.out.println("Developing a skill requires dedication and effort");
    }

    public void hasLevels() {
        System.out.println("Skill has different levels of proficiency");
    }
}
