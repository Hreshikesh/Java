package com.xworkz.isarelation.Internal;

public class Cycle {
    public Cycle() {
        System.out.println("Running no-arg constructor of Cycle");
    }

    public void pedal() {
        System.out.println("Cycle moves by pedaling");
    }

    public void brake() {
        System.out.println("Cycle has brakes");
    }

    public void wheels() {
        System.out.println("Cycle has two wheels");
    }

    public void ringBell() {
        System.out.println("Cycle has a bell");
    }

    public void stand() {
        System.out.println("Cycle has a side stand");
    }
}
