package com.xworkz.isarelation.Internal;

public class Tool {
    public Tool() {
        System.out.println("Running no-arg constructor of Tool");
    }

    public void use() {
        System.out.println("Using the tool");
    }

    public void maintain() {
        System.out.println("Maintaining the tool");
    }

    public void store() {
        System.out.println("Storing the tool");
    }

    public void clean() {
        System.out.println("Cleaning the tool");
    }

    public void repair() {
        System.out.println("Repairing the tool");
    }
}
