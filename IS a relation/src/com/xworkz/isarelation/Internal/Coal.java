package com.xworkz.isarelation.Internal;

public class Coal {
    public Coal() {
        System.out.println("Running no-arg constructor of Coal");
    }

    public void burn() {
        System.out.println("Coal is used as a fuel for burning");
    }

    public void generateEnergy() {
        System.out.println("Coal is used to generate electricity and heat");
    }

    public void carbonContent() {
        System.out.println("Coal contains high carbon content");
    }

    public void extraction() {
        System.out.println("Coal is extracted from mines");
    }

    public void usage() {
        System.out.println("Coal is used in industries, power plants, and households");
    }
}
